#!/usr/bin/env bash
# =============================================================================
# Полная репликация пайплайна анализа тональности (от сырых данных до фигур).
#
# Запуск:   bash run_all.sh
# Окружение: PY  — интерпретатор Python с установленным requirements.txt
#            GPU — список видимых GPU для тяжёлых стадий (по умолчанию 0)
#   PY=python GPU=0,1,2,3 bash run_all.sh
#
# ВАЖНО: в репозитории через Git LFS уже лежат master.parquet, эмбеддинги,
# обученные модели и предсказания. Если они подтянуты (`git lfs pull`), стадии
# 0–5 можно пропустить и сразу открыть sentiment_analysis.ipynb (стадия 6).
# Стадии ниже описывают сборку «с нуля».
#
# Требования к железу: 1× GPU (разрабатывалось на Tesla V100 32 ГБ).
# =============================================================================
set -euo pipefail
cd "$(dirname "$0")"

PY="${PY:-/home/zabolonkov/.conda/envs/topic-sentiment/bin/python}"
GPU="${GPU:-0}"
step() { echo; echo "=================  $*  ================="; }

# -----------------------------------------------------------------------------
# Стадия 0. Сырые данные
# -----------------------------------------------------------------------------
# Часть источников доступна публично на Hugging Face — их скачивает download_hf.
# Остальные (Kaggle-датасеты и репозиторий searayeah) скачиваются вручную в
# data/raw/ — см. ссылки в datasets_sources.md. Ручная разметка для OOB-теста
# (соцсети) лежит в data/combined_platform_proportional_sample.xlsx.
step "0/ скачивание публичных HF-источников"
"$PY" src/download_hf.py
# "$PY" src/inspect_searayeah.py   # (опц.) осмотр сырых схем searayeah

# -----------------------------------------------------------------------------
# Стадия 1. Гармонизация и сборка мастер-таблицы
# -----------------------------------------------------------------------------
# harmonize: привести каждый источник к единой схеме -> data/interim/concat_raw.parquet
# build_master: дедуп по приоритету разметки (human > rating > distant),
#   инженерия текстовых признаков, стратифицированные сплиты A/B
#   -> data/processed/master.parquet  (≈1.37 млн строк, 24 признака)
step "1/ гармонизация источников"
"$PY" src/harmonize.py
step "1/ сборка master.parquet"
"$PY" src/build_master.py

# -----------------------------------------------------------------------------
# Стадия 2. Эмбеддинги всего корпуса (USER-bge-m3, L2-норм, float16)
# -----------------------------------------------------------------------------
step "2/ эмбеддинги master (multi-GPU)"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/embed.py

# -----------------------------------------------------------------------------
# Стадия 3. Файн-тюнинг энкодера (поиск по 4 кандидатам + финальное обучение A/B)
# -----------------------------------------------------------------------------
# Дефолты воспроизводят финальный прогон: 128 триалов случайного поиска,
# подбор на варианте A, балансировка по классам 15k (поиск) / 50k (финал).
# Переопределяется через FT_N_TRIALS / FT_SEARCH_VARIANT / FT_PER_CLASS_*.
step "3/ файн-тюнинг (поиск + финал A/B)"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/finetune.py

# -----------------------------------------------------------------------------
# Стадия 4. OOB-набор (ручная разметка соцсетей) + его эмбеддинги
# -----------------------------------------------------------------------------
step "4/ сборка OOB-теста"
"$PY" src/prep_oob.py
step "4/ эмбеддинги OOB"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/embed_oob.py

# -----------------------------------------------------------------------------
# Стадия 5. Предсказания на OOB: наши классические, наши файн-тюн, внешние (12)
# -----------------------------------------------------------------------------
step "5/ классические модели (обучение A/B + OOB)"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/oob_classical.py
step "5/ файн-тюн энкодер на OOB"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/oob_finetune_predict.py
step "5/ внешние pretrained-модели на OOB"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" src/oob_external.py

# -----------------------------------------------------------------------------
# Стадия 6. Ноутбук: EDA, кластеризация, сравнение моделей, анализ OOB -> фигуры
# -----------------------------------------------------------------------------
step "6/ прогон ноутбука -> reports/figures/"
CUDA_VISIBLE_DEVICES="$GPU" "$PY" -m jupyter nbconvert --to notebook --execute \
    --ExecutePreprocessor.timeout=1800 --inplace sentiment_analysis.ipynb

step "ГОТОВО"
